"use strict"

/* Operadores de Relacao:
    >   -> Maior
    >=  -> Maior ou Igual
    <   -> Menor
    <=  -> Menor ou Igual
    ==  -> Igual ( comparacao )
    !=  -> Diferente
*/


let num1 = 10, num2 = 5, num3 = 10

console.log(num1 > num2) // return true
console.log(num1 < num2) // return false
console.log(num1 >= num3) // return true
console.log(num1 == num3) // return true
console.log(!(num1 == num3)) // return false --------> Operação de Negação
console.log(num1 != num3) // return false -----------> Operação de Diferença
